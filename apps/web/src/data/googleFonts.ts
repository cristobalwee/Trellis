const fontFamilyParam = (family: string) => family.trim().replace(/\s+/g, '+');

export function getGoogleFontHref(family: string, weighted = true): string {
  const familyParam = fontFamilyParam(family);
  const axis = weighted ? ':wght@100..900' : '';
  return `https://fonts.googleapis.com/css2?family=${familyParam}${axis}&display=swap`;
}

export function appendGoogleFontStylesheet(family: string, id: string): void {
  if (document.getElementById(id)) return;

  const link = document.createElement('link');
  link.id = id;
  link.rel = 'stylesheet';
  link.href = getGoogleFontHref(family);
  link.onerror = () => {
    link.onerror = null;
    link.href = getGoogleFontHref(family, false);
  };
  document.head.appendChild(link);
}
